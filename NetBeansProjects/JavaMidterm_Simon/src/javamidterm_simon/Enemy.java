package javamidterm_simon;

public class Enemy {
    
    public static int x;
    public static int y;
    
    public Enemy(int a, int b) {
        
        x = a;
        y = b;
        
    }
    
    public int getX() {
        return x;
    }
    
    public int getY() {
        return y;
    }
    
}
